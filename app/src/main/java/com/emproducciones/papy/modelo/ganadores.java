package com.emproducciones.papy.modelo;

public class ganadores {

    private Integer id;
    private Integer idNoche;
    private Integer idLotoJugador;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getIdNoche() {
        return idNoche;
    }

    public void setIdNoche(Integer idNoche) {
        this.idNoche = idNoche;
    }

    public Integer getIdLotoJugador() {
        return idLotoJugador;
    }

    public void setIdLotoJugador(Integer idLotoJugador) {
        this.idLotoJugador = idLotoJugador;
    }


}
